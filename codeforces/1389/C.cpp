    auto Try = [&](int x, int y) {
        bool cur = 1; int res = 0, tot = 0;
        for(int z = 0; z < n; z++) {
            if(cur) {
                if(s[z]-'0' != x) res++;
                else tot++, cur ^= 1;
            }
            else {
                if(s[z]-'0' != y) res++;
                else tot++, cur ^= 1;                
            }
        }
        if(tot&1) res++;
        chmin(ans, res);
    };

